void main() {
  final summary = {
    ...mapBefore2010,
    ...mapAfter2010,
  };

  final allMachinery =
      summary.values.fold(<AgriculturalMachinery>[], (list, territory) {
    territory.forEach((t) => list += t.machineries);
    return list;
  }).toSet();

  final total = allMachinery.fold(0, (sum, machinery) {
    final diffInYears =
        (DateTime.now().difference(machinery.releaseDate)).inDays / 365;
    sum += diffInYears.toInt();
    return sum;
  });

  final list = allMachinery.toList();
  list.sort((a, b) => a.releaseDate.compareTo(b.releaseDate));
  final firstHalf = list.take(list.length ~/ 2);

  final totalHalf = firstHalf.fold(0, (sum, machinery) {
    final diffInYears =
        (DateTime.now().difference(machinery.releaseDate)).inDays / 365;
    sum += diffInYears.toInt();
    return sum;
  });

  print('Средний возраст всей техники: ${total / allMachinery.length}');
  print(
      'Средний взраст 50% самой старой техники: ${totalHalf / firstHalf.length}');
}

enum Countries { brazil, russia, turkish, spain, japan }

class Territory {
  int areaInHectare;
  List<String> crops;
  List<AgriculturalMachinery> machineries;

  Territory(
    this.areaInHectare,
    this.crops,
    this.machineries,
  );
}

class AgriculturalMachinery {
  final String id;
  final DateTime releaseDate;

  AgriculturalMachinery(
    this.id,
    this.releaseDate,
  );

  // Переопределяем оператор "==", что бы сравнивать объекты по значению
  @override
  bool operator ==(Object? other) {
    if (other is! AgriculturalMachinery) return false;
    if (other.id == id && other.releaseDate == releaseDate) return true;

    return false;
  }

  @override
  int get hashCode => id.hashCode ^ releaseDate.hashCode;
}

